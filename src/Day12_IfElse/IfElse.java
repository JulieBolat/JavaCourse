package Day12_IfElse;

public class IfElse {
    public static void main(String[] args) {

        //Instead of creating two IFs, we can use ELSE.
        //else means if not!

        int income = 1000;

        if (income > 100_000){

            System.out.println("Your income is good");

        }else {

            System.out.println("Your income is not good");

        }

    }
}
