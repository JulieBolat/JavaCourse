package Day4.TypeCasting;

public class CharConverting {

    public static void main(String[] args) {

        //M

        char c= 'M';
        System.out.println(c);
        int d= (char)c;
        System.out.println(d);

        char c1= 'M';
        int d1= c1;
        System.out.println(d1);

        char var1= 'a';
        int var2= (char)var1;
        System.out.println(var2);




    }
}
