package Day2;


public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("I learn \"Java\" in TechnoStudy");
        System.out.println("C:\\ProgramFiles\\...\\TEST\\");
        System.out.println("Java is good\nprogramming language");
        System.out.println("TechnoStudy");
        System.out.println("Techno\tStudy");

        System.out.println("TECHNO");
        System.out.println("S\nT\nU\nD\nY\n");

        System.out.println("Today is Friday, \"April 23rd\"");
        System.out.println("Today is Friday, \\April 23rd\\");

        System.out.println("Today is Friday: \nApril 23rd");
        System.out.println("Today is Friday \t April 23rd");



    }
}
