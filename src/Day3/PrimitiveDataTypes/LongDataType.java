package Day3.PrimitiveDataTypes;

public class LongDataType {
    public static void main(String[] args) {
        long viewCount = 3123457890L;
        System.out.println(viewCount);

        long myFirstLong = 76_527_384_910L;
        long mySecondLong = -6_876_654_457L;
        System.out.println(myFirstLong);
        System.out.println(mySecondLong);

        long maxValue = 9223372036854775807L;
        long minValue = -9223372036854775807L;

        System.out.println(maxValue);
        System.out.println(minValue);


    }
}
