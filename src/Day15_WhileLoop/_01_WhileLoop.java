package Day15_WhileLoop;

public class _01_WhileLoop {

    public static void main(String[] args) {
        //For Loop

        for (int i= 1; i<=5; i++) {
            System.out.println("Hello World" + " " + i);
        }

        System.out.println(" ");

        //While Loop
        int counter= 1;
        while (counter<=5) {
            System.out.println("Hello World" + " " + counter);
            counter++;


        }

    }
}
